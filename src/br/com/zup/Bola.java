package br.com.zup;

public class Bola {
    String circuferencia;
    String cor;
    String marca;
    String velocidade;

    public Bola(String circuferencia, String cor, String marca, String velocidade) {
        this.circuferencia = circuferencia;
        this.cor = cor;
        this.marca = marca;
        this.velocidade = velocidade;
    }
}
